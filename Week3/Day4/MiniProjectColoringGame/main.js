for(let i = 0; i < 900; i++){
    let cell = document.createElement('div');
    cell.classList.add('cell');
    cell.addEventListener('mousedown', setColor)
    cell.addEventListener('mouseover', overColor)
    cell.addEventListener('mouseup', ()=> mouseDown = false)
    
    gridDiv.append(cell);
}
